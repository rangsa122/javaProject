package Operators;

public class IncreaseOperator {

	public static void main(String[] args) {
		// 단항 연산자
		int x = 10;
		int y = 10;
		int z;
		
		System.out.println("-----------------------");
		
		// 증감 연산자를 단독으로 사용하면 연산자의 위치는 상관없다.
		x++;
		++x;
		System.out.println(x);
		
		// 논리 부정 연산
		
		
	}

}
